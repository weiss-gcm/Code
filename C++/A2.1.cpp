#include <iostream>
using namespace std;

int main() {
  int a = 5;
  int b = 10;
  int c = a + b;
  int d = a * b;
  cout << "The product of " << a << " and " << b << " is " << d << "." << endl;
  cout << "The difference of " << a << " and " << b << " is " << (b - a) << "." << endl;
  cout << "The sum of " << a << " and " << b << " is " << c << "." << endl;
  return 0;
}
